function initQuiz7(){
  quizState7.q=[...questions7].sort(()=>Math.random()-.5);
  quizState7.idx=0;quizState7.correct=0;quizState7.answered=false;
  document.getElementById('quiz7-body').style.display='block';
  document.getElementById('quiz7-result').style.display='none';
  renderQuestion7();
}

function renderQuestion7(){
  const q=quizState7.q[quizState7.idx];
  document.getElementById('q7-counter').innerText=`${quizState7.idx+1} / ${quizState7.q.length}`;
  document.getElementById('q7-text').innerText=q.q;
  const letters=['A','B','C'];
  const shuffled=[0,1,2].sort(()=>Math.random()-.5);
  document.getElementById('q7-answers').innerHTML=shuffled.map((origIdx,i)=>`
    <button class="answer-btn" data-correct="${origIdx===q.correct}" onclick="answerQ7(this)">
      <span class="answer-letter">${letters[i]}</span>${q.a[origIdx]}
    </button>`).join('');
  const dots=document.getElementById('progress7-dots');
  dots.innerHTML=Array.from({length:quizState7.q.length},(_,i)=>`<div class="dot ${i<quizState7.idx?(quizState7.q[i]._result==='c'?'correct':'wrong'):''}${i===quizState7.idx?'current':''}"></div>`).join('');
  document.getElementById('feedback7').style.display='none';
  document.getElementById('next7-btn').style.display='none';
  quizState7.answered=false;
}

function answerQ7(btn){
  if(quizState7.answered)return;
  quizState7.answered=true;
  const isCorrect=btn.dataset.correct==='true';
  document.querySelectorAll('#q7-answers .answer-btn').forEach(b=>{
    b.disabled=true;
    if(b.dataset.correct==='true')b.classList.add('correct');
  });
  if(isCorrect){
    btn.classList.add('correct');
    quizState7.correct++;
    quizState7.q[quizState7.idx]._result='c';
    const fb=document.getElementById('feedback7');
    fb.className='feedback-box correct';fb.innerText='🎉 Helyes! Zseniális válasz!';fb.style.display='block';
    startConfetti(30);
    if(window.mascotCorrect)mascotCorrect();
  } else {
    btn.classList.add('wrong');
    quizState7.q[quizState7.idx]._result='w';
    const fb=document.getElementById('feedback7');
    fb.className='feedback-box wrong';fb.innerText='❌ Sajnos nem ez volt a helyes válasz. Nézd meg a zöld gombot!';fb.style.display='block';
    if(window.mascotWrong)mascotWrong();
  }
  document.getElementById('next7-btn').style.display='block';
  document.getElementById('next7-btn').innerText=quizState7.idx===quizState7.q.length-1?'Eredmény megtekintése 🏆':'Következő kérdés →';
}

function nextQuestion7(){
  quizState7.idx++;
  if(quizState7.idx<quizState7.q.length){renderQuestion7();}
  else{showResult7();}
}

function showResult7(){
  document.getElementById('quiz7-body').style.display='none';
  const res=document.getElementById('quiz7-result');
  res.style.display='block';
  const score=quizState7.correct;
  const emoji=score>=10?'🏆':score>=8?'🥇':score>=6?'🥈':score>=4?'🥉':'💪';
  const msg=score>=10?'Fenomenális! Igazi mesterlövész vagy!':score>=8?'Kiváló munka, felderítő!':score>=6?'Szép teljesítmény! Még egy kis gyakorlás...':score>=4?'Jó próbálkozás! Olvass még egy kicsit!':'Gyakorolj sokat, ügynök!';
  const earnedXP=score*15;
  addXP(earnedXP);
  let unlockHtml='';
  if(score>=10){
    unlockHtml=`<div class="unlock-notice">🔓 Következő dosszié feloldható! A kód: <strong>corvinák</strong></div>`;
  } else {
    unlockHtml=`<div class="unlock-notice" style="background:var(--rose-l);border-color:var(--rose);color:var(--rose-d);">⚠️ Legalább 10/12 pont kell a következő dosszié kódjához! Próbáld újra!</div>`;
  }
  res.innerHTML=`<div class="result-emoji">${emoji}</div>
    <div class="result-score">${score} / 12</div>
    <div class="result-msg">${msg}</div>
    <div class="result-xp">⭐ +${earnedXP} XP megszerezve!</div>
    ${unlockHtml}
    <button class="retry-btn" onclick="initQuiz7()">🔄 Újra próbálom</button>`;
}
