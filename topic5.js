function renderQuestion5(){
  const q=quizState5.q[quizState5.idx];
  document.getElementById('q5-counter').innerText=`${quizState5.idx+1} / ${quizState5.q.length}`;
  document.getElementById('q5-text').innerText=q.q;
  const letters=['A','B','C'];
  const shuffled=[0,1,2].sort(()=>Math.random()-.5);
  document.getElementById('q5-answers').innerHTML=shuffled.map((origIdx,i)=>`
    <button class="answer-btn" data-correct="${origIdx===q.correct}" onclick="answerQ5(this)">
      <span class="answer-letter">${letters[i]}</span>${q.a[origIdx]}
    </button>`).join('');
  const dots=document.getElementById('progress5-dots');
  dots.innerHTML=Array.from({length:quizState5.q.length},(_,i)=>`<div class="dot ${i<quizState5.idx?(quizState5.q[i]._result==='c'?'correct':'wrong'):''}${i===quizState5.idx?'current':''}"></div>`).join('');
  document.getElementById('feedback5').style.display='none';
  document.getElementById('next5-btn').style.display='none';
  quizState5.answered=false;
}

function answerQ5(btn){
  if(quizState5.answered)return;
  quizState5.answered=true;
  const isCorrect=btn.dataset.correct==='true';
  document.querySelectorAll('#q5-answers .answer-btn').forEach(b=>{
    b.disabled=true;
    if(b.dataset.correct==='true')b.classList.add('correct');
  });
  if(isCorrect){
    btn.classList.add('correct');
    quizState5.correct++;
    quizState5.q[quizState5.idx]._result='c';
    const fb=document.getElementById('feedback5');
    fb.className='feedback-box correct';fb.innerText='🎉 Helyes! Zseniális válasz!';fb.style.display='block';
    startConfetti(30);
    if(window.mascotCorrect)mascotCorrect();
  } else {
    btn.classList.add('wrong');
    quizState5.q[quizState5.idx]._result='w';
    const fb=document.getElementById('feedback5');
    fb.className='feedback-box wrong';fb.innerText='❌ Sajnos nem ez volt a helyes válasz. Nézd meg a zöld gombot!';fb.style.display='block';
    if(window.mascotWrong)mascotWrong();
  }
  document.getElementById('next5-btn').style.display='block';
  document.getElementById('next5-btn').innerText=quizState5.idx===quizState5.q.length-1?'Eredmény megtekintése 🏆':'Következő kérdés →';
}

function nextQuestion5(){
  quizState5.idx++;
  if(quizState5.idx<quizState5.q.length){renderQuestion5();}
  else{showResult5();}
}

