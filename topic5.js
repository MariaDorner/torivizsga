function t5AnimateBars(){
  document.querySelectorAll('#t5bars [data-width]').forEach(function(el){
    el.style.width = el.getAttribute('data-width');
  });
}
document.addEventListener('DOMContentLoaded', function(){
  if('IntersectionObserver' in window){
    var obs = new IntersectionObserver(function(entries){
      if(entries[0].isIntersecting){ t5AnimateBars(); obs.disconnect(); }
    },{threshold:0.3});
    var target = document.getElementById('t5bars');
    if(target) obs.observe(target);
  } else {
    setTimeout(t5AnimateBars, 800);
  }
});

