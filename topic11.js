function initQuiz11(){
  quizState11.q=[...questions11].sort(()=>Math.random()-.5);
  quizState11.idx=0;quizState11.correct=0;quizState11.answered=false;
  document.getElementById('quiz11-body').style.display='block';
  document.getElementById('quiz11-result').style.display='none';
  renderQuestion11();
}

function renderQuestion11(){
  const q=quizState11.q[quizState11.idx];
  document.getElementById('q11-counter').innerText=`${quizState11.idx+1} / ${quizState11.q.length}`;
  document.getElementById('q11-text').innerText=q.q;
  const letters=['A','B','C'];
  const shuffled=[0,1,2].sort(()=>Math.random()-.5);
  document.getElementById('q11-answers').innerHTML=shuffled.map((origIdx,i)=>`
    <button class="answer-btn" data-correct="${origIdx===q.correct}" onclick="answerQ11(this)">
      <span class="answer-letter">${letters[i]}</span>${q.a[origIdx]}
    </button>`).join('');
  const dots=document.getElementById('progress11-dots');
  dots.innerHTML=Array.from({length:quizState11.q.length},(_,i)=>`<div class="dot ${i<quizState11.idx?(quizState11.q[i]._result==='c'?'correct':'wrong'):''}${i===quizState11.idx?'current':''}"></div>`).join('');
  document.getElementById('feedback11').style.display='none';
  document.getElementById('next11-btn').style.display='none';
  quizState11.answered=false;
}

function answerQ11(btn){
  if(quizState11.answered)return;
  quizState11.answered=true;
  const isCorrect=btn.dataset.correct==='true';
  document.querySelectorAll('#q11-answers .answer-btn').forEach(b=>{
    b.disabled=true;
    if(b.dataset.correct==='true')b.classList.add('correct');
  });
  if(isCorrect){
    btn.classList.add('correct');
    quizState11.correct++;
    quizState11.q[quizState11.idx]._result='c';
    const fb=document.getElementById('feedback11');
    fb.className='feedback-box correct';fb.innerText='🎉 Helyes! Zseniális válasz!';fb.style.display='block';
    startConfetti(30);
    if(window.mascotCorrect)mascotCorrect();
  } else {
    btn.classList.add('wrong');
    quizState11.q[quizState11.idx]._result='w';
    const fb=document.getElementById('feedback11');
    fb.className='feedback-box wrong';fb.innerText='❌ Sajnos nem ez volt a helyes válasz. Nézd meg a zöld gombot!';fb.style.display='block';
    if(window.mascotWrong)mascotWrong();
  }
  document.getElementById('next11-btn').style.display='block';
  document.getElementById('next11-btn').innerText=quizState11.idx===quizState11.q.length-1?'Eredmény megtekintése 🏆':'Következő kérdés →';
}

function nextQuestion11(){
  quizState11.idx++;
  if(quizState11.idx<quizState11.q.length){renderQuestion11();}
  else{showResult11();}
}

function showResult11(){
  document.getElementById('quiz11-body').style.display='none';
  const res=document.getElementById('quiz11-result');
  res.style.display='block';
  const score=quizState11.correct;
  const emoji=score>=10?'🏆':score>=8?'🥇':score>=6?'🥈':score>=4?'🥉':'💪';
  const msg=score>=10?'Fenomenális! Igazi mesterlövész vagy!':score>=8?'Kiváló munka, felderítő!':score>=6?'Szép teljesítmény! Még egy kis gyakorlás...':score>=4?'Jó próbálkozás! Olvass még egy kicsit!':'Gyakorolj sokat, ügynök!';
  const earnedXP=score*15;
  addXP(earnedXP);
  let unlockHtml='';
  if(score>=10){
    unlockHtml=`<div class="unlock-notice">🔓 Következő dosszié feloldható! A kód: <strong>Wittenberg</strong></div>`;
  } else {
    unlockHtml=`<div class="unlock-notice" style="background:var(--rose-l);border-color:var(--rose);color:var(--rose-d);">⚠️ Legalább 10/12 pont kell a következő dosszié kódjához! Próbáld újra!</div>`;
  }
  res.innerHTML=`<div class="result-emoji">${emoji}</div>
    <div class="result-score">${score} / 12</div>
    <div class="result-msg">${msg}</div>
    <div class="result-xp">⭐ +${earnedXP} XP megszerezve!</div>
    ${unlockHtml}
    <button class="retry-btn" onclick="initQuiz11()">🔄 Újra próbálom</button>`;
}
