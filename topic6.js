  const score=quizState6.correct;
  const emoji=score>=10?'🏆':score>=8?'🥇':score>=6?'🥈':score>=4?'🥉':'💪';
  const msg=score>=10?'Fenomenális! Igazi mesterlövész vagy!':score>=8?'Kiváló munka, felderítő!':score>=6?'Szép teljesítmény! Még egy kis gyakorlás...':score>=4?'Jó próbálkozás! Olvass még egy kicsit!':'Gyakorolj sokat, ügynök!';
  const earnedXP=score*15;
  addXP(earnedXP);
  let unlockHtml='';
  if(score>=8){
    unlockHtml=`<div class="unlock-notice">🔓 Következő dosszié feloldható! A kód: <strong>1456</strong></div>`;
  } else {
    unlockHtml=`<div class="unlock-notice" style="background:var(--rose-l);border-color:var(--rose);color:var(--rose-d);">⚠️ Legalább 8/12 pont kell a következő dosszié kódjához! Próbáld újra!</div>`;
  }
